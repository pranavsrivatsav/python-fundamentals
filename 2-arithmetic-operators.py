# arithmetic operators works only with integers and floats

# addition
print(2 + 3)
# 5

# subtraction
print(2 - 3)  # -1

# multiplication
print(2 * 3)  # 6

# division
print(2 / 3)  # 0.6666666666666666

# modulo
print(2 % 3)  # 2

# exponentiation
print(2**3)  # 8

# floor - floor of the quotient
print(2 // 3)  # 0
